package classes;

import java.util.Date;

/**
 *
 * @author luxu
 */
public class Garcon {

    private Integer garCodigo;
    private String garNome;
    private String garEndereco;
    private String garTelefone;
    private Date garDataAdmissao;
    private String garRg;
    private String garCpf;

    public Integer getGarCodigo() {
        return garCodigo;
    }

    public void setGarCodigo(Integer garCodigo) {
        this.garCodigo = garCodigo;
    }

    public String getGarNome() {
        return garNome;
    }

    public void setGarNome(String garNome) {
        this.garNome = garNome;
    }

    public String getGarEndereco() {
        return garEndereco;
    }

    public void setGarEndereco(String garEndereco) {
        this.garEndereco = garEndereco;
    }

    public String getGarTelefone() {
        return garTelefone;
    }

    public void setGarTelefone(String garTelefone) {
        this.garTelefone = garTelefone;
    }

    public Date getGarDataAdmissao() {
        return garDataAdmissao;
    }

    public void setGarDataAdmissao(Date garDataAdmissao) {
        this.garDataAdmissao = garDataAdmissao;
    }

    public String getGarRg() {
        return garRg;
    }

    public void setGarRg(String garRg) {
        this.garRg = garRg;
    }

    public String getGarCpf() {
        return garCpf;
    }

    public void setGarCpf(String garCpf) {
        this.garCpf = garCpf;
    }
}
